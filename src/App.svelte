    <p class="lv_Par">El crecimiento se produce en todo el territorio europeo pero en términos absolutos los cambios más drásticos se registran en los países sureños que bordean el mediterráneo. España es después de Chipre, Malta, Grecia y Portugal, el quinto país de Europa que ha registrado un mayor aumento, según el valor de enfriamiento que elabora el Centro Común de Investigación de la Comisión Europea —JRC, por sus siglas en inglés—. Esta métrica sirve para estimar el impacto que las altas temperaturas tienen sobre la demanda energética de un país y técnicamente se expresa en grados día.

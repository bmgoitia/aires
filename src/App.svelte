    <p class="lv_Par">El cálculo parte de los 24°C, la temperatura veraniega de confort en la que no se necesita utilizar el aire acondicionado. Si un día la temperatura media es de 30°C, significará que ese día hay 6°C de diferencia entre la temperatura de confort y la temperatura exterior. Si al cabo del año sumamos todos estos grados de más que ha habido respecto a la temperatura de confort obtendremos el esfuerzo de refrigeración que ha tenido que hacer todo el país en un año. Por ejemplo, España durante los años ochenta tenía una media de 153°C acumulados al año de refrigeración. En la última década ha tenido una media de 259°C. Significa que el esfuerzo de refrigeración que tiene que hacer el país para mantener sus edificios en una temperatura confortable se ha multiplicado por 1,7. 
